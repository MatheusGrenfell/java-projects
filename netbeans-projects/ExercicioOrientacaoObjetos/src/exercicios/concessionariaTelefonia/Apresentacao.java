    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Telefone t;
            if (jRadioButton1.isSelected()) {
                t = new TelefoneResidencial(jTextField2.getText(), jTextField1.getText(), df.parse(jTextField4.getText()), jTextField3.getText(), jCheckBox1.isSelected());
            } else {
                t = new TelefoneComercial(jTextField2.getText(), jTextField1.getText(), df.parse(jTextField4.getText()), jTextField3.getText(), Integer.parseInt(jTextField5.getText()));
            }
            this.redeTelefonica.put(t.getNumero(), t);
            JOptionPane.showMessageDialog(this, "Valor básico: " + t.getValorBasico(), "Info", JOptionPane.INFORMATION_MESSAGE);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Data inválida", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double total = 0.0d;
        for (Telefone t : redeTelefonica.values()) {
            total += t.getValorBasico();
        }
        JOptionPane.showMessageDialog(this, "Valor do faturamento: " + total, "Info", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Telefone t = redeTelefonica.get(jTextField6.getText());
            JOptionPane.showMessageDialog(this, t.exibirDados(), "Info", JOptionPane.INFORMATION_MESSAGE);
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Número não encontrado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

