    private void incluirCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incluirCategoriaActionPerformed
        if (txtCategoria.getText() == null || txtCategoria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome da Categoria deve ser Informado!");
        } else {
            if (txtPotencia.getText() == null || txtPotencia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Potencia deve ser Informada!");
            } else {

                try {

                    int potencia;

                    try {
                        potencia = Integer.parseInt(txtPotencia.getText());
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(this, "Valor Potência Incorreto: " + e.getMessage());
                        return;
                    }

                    Categoria novaCategoria = new Categoria(txtCategoria.getText(),
                            potencia);

                    listaCategoria.put(txtCategoria.getText(), novaCategoria);

                    JOptionPane.showMessageDialog(this, "Categoria Cadastrada!");
                    categoriaComodoComboBox.addItem(txtCategoria.getText());
                    txtCategoria.setText(" ");
                    txtPotencia.setText(" ");
                    txtCategoria.grabFocus();

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao Cadastrar Categoria!");
                }
            }
        }
    }//GEN-LAST:event_incluirCategoriaActionPerformed

