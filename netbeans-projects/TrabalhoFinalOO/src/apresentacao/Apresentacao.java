    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Edificacao e = null;
            if (TabbedPannel.getSelectedIndex() == Abas.residencial.valor) {

                if (ResRes.getText() == null || ResRes.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Responsavel");
                }

                if (NumRes.getText() == null || NumRes.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Numero");
                }

                if (EndRes.getText() == null || EndRes.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Endereço");
                }

                if (QtdRes.getText() == null || QtdRes.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Qtd. Moradores");
                }

                if (TipoRes.getText() == null || TipoRes.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Tipo");
                }

                e = new Residencial(ResRes.getText(),
                        EndRes.getText(),
                        Integer.parseInt(NumRes.getText()),
                        Integer.parseInt(QtdRes.getText()),
                        TipoRes.getText());
                edificacoes.put(ResRes.getText(), e);
                edificacoesComodoComboBox.addItem(ResRes.getText());
                edificacoesCalcularComboBox.addItem(ResRes.getText());
            } else {

                if (ResCom.getText() == null || ResCom.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Empresa");
                }

                if (EndCom.getText() == null || EndCom.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Endereço");
                }

                if (NumCom.getText() == null || NumCom.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Numero");
                }

                if (TipoCom.getText() == null || TipoCom.getText().isEmpty()) {
                    throw new ArgumentoNuloException("Ramo de Atividade");
                }

                e = new Comercial(ResCom.getText(),
                        EndCom.getText(),
                        Integer.parseInt(NumCom.getText()),
                        TipoCom.getText());
                edificacoes.put(ResCom.getText(), e);
                edificacoesComodoComboBox.addItem(ResCom.getText());
                edificacoesCalcularComboBox.addItem(ResCom.getText());
            }

            JOptionPane.showMessageDialog(this, "Categoria Cadastrada!");
            LimparCamposEdificacao();

        } catch (MinhaException me) {
            System.err.println(me.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btIncluirComodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIncluirComodoActionPerformed
        try {

            Edificacao t = edificacoes.get(edificacoesComodoComboBox.getSelectedItem().toString());

            Categoria c = listaCategoria.get(categoriaComodoComboBox.getSelectedItem().toString());

            double largura = 0;
            double comprimento = 0;
            try {
                largura = Double.parseDouble(txtLargura.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Valor Largura Invalido: " + e.getMessage());
                return;
            }

            try {
                comprimento = Double.parseDouble(txtComprimento.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Valor Comprimento Invalido: " + e.getMessage());
                return;
            }

            t.addComodo(new Comodo(largura, comprimento, c));

            JOptionPane.showMessageDialog(this, "Comodo Adicionado!");
            LimparCamposComodo();
        } catch (NullPointerException npe) {
            JOptionPane.showMessageDialog(this, "Erro ao Incluir Comodo!");
        }

    }//GEN-LAST:event_btIncluirComodoActionPerformed

