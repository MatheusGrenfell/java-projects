	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
	{//GEN-HEADEREND:event_jButton1ActionPerformed
            int retorno;
            do {
                JFileChooser chooser = new JFileChooser();
                chooser.setSelectedFile(new File("OntologiaCarros.rdf"));
                retorno = chooser.showOpenDialog(null);
                if (retorno == JFileChooser.APPROVE_OPTION) {
                    String caminho = chooser.getSelectedFile().getAbsolutePath();
                    negocio.carregarOntologia(caminho);
                } else {
                    retorno = JFileChooser.CANCEL_OPTION;
                }
            } while ((retorno != JFileChooser.APPROVE_OPTION) && (retorno != JFileChooser.CANCEL_OPTION) && (retorno != JFileChooser.ABORT));
	}//GEN-LAST:event_jButton1ActionPerformed

