package serverDB;

public class DadosControle {

    private int codNovo, codAntigo, qtdade;

    public int getCodNovo() {
        return codNovo;
    }

    public void setCodNovo(int codNovo) {
        this.codNovo = codNovo;
    }

    public int getCodAntigo() {
        return codAntigo;
    }

    public void setCodAntigo(int codAntigo) {
        this.codAntigo = codAntigo;
    }

    public int getQtdade() {
        return qtdade;
    }

    public void setQtdade(int qtdade) {
        this.qtdade = qtdade;
    }
}
