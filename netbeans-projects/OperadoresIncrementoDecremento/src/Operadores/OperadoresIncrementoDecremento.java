package Operadores;


public class OperadoresIncrementoDecremento {

    public static void main(String[] args) {
        int c, x;
        c = 5;
        x = 105;

        System.out.println("OPERADOR DE INCREMENTO");
        System.out.println(c);
        System.out.println(c++);
        System.out.println(c);
        System.out.println();

        System.out.println(c);
        System.out.println(++c);
        System.out.println(c);
        System.out.println();

        System.out.println("OPERADORES DE DECREMENTO");
        System.out.println(x);
        System.out.println(x--);
        System.out.println(x);
        System.out.println();

        System.out.println(x);
        System.out.println(--x);
        System.out.println(x);
        System.out.println();
    }
}
