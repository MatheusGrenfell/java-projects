package Exe7;

public class Palavra {

    private String portugues, ingles;

    public Palavra(String portugues, String ingles) {
        this.portugues = portugues;
        this.ingles = ingles;
    }

    public String getPortugues() {
        return portugues;
    }

    public String getIngles() {
        return ingles;
    }
}
