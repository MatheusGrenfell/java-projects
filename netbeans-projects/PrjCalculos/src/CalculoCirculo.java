
public class CalculoCirculo {

    public static void main(String[] args) {
        double umNumero, outroNumero, result;
        umNumero = 64;
        outroNumero = Math.PI;
        result = Math.sqrt(umNumero);
        System.out.println("A Raiz = " + result);
        System.out.println("A Constante PI =" + outroNumero);
        result = outroNumero * outroNumero;
        System.out.println("Pi ao quadrado = " + result);
        result = Math.sqrt(3);
        System.out.println("Raiz de 3 = " + result);
    }
}
